//Primitives
console.log("10 == 10 ->" ,10 == 10); //true
console.log("'Neel' == 'Neel' ->",'Neel' == 'Neel'); //true
console.log("false == false ->", false == false); //true
console.log("null == null ->", null == null);

//Non Primitives
console.log("[] == [] ->", [] == []); //false
console.log("[10,20] == [10,20] ->", [10,20] == [10,20]); //false
console.log("{} == {} ->", {} == {}); //false
console.log("{ a:10, b:20 } == { a:10, b:20 } ->", { a:10, b:20 } == { a:10, b:20 }); //false